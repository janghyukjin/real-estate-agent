def main():
    # 기존 bjdong_codes 로드
    bjdong_path = os.path.join(DATA_DIR, "bjdong_codes.json")
    with open(bjdong_path) as f:
        bjdong_codes = json.load(f)

    print(f"기존 bjdong_codes: {len(bjdong_codes)}개 시군구")

    # 새 코드 추가
    new_codes = get_known_bjdong_codes()
    added = 0
    for code, dongs in new_codes.items():
        if code not in bjdong_codes:
            bjdong_codes[code] = dongs
            added += 1
            print(f"  추가: {code} ({NEW_REGIONS.get(code, '?')}) - {len(dongs)}개 동")
        else:
            # 기존에 있으면 병합
            existing = bjdong_codes[code]
            new_dongs = {k: v for k, v in dongs.items() if k not in existing}
            if new_dongs:
                existing.update(new_dongs)
                print(f"  병합: {code} - {len(new_dongs)}개 동 추가")

    # 기존 지역 확인
    for code in CHECK_EXISTING:
        status = "있음" if code in bjdong_codes else "없음"
        print(f"  확인: {code} - {status}")

    # 저장
    with open(bjdong_path, "w") as f:
        json.dump(bjdong_codes, f, ensure_ascii=False, indent=2)

    print(f"\n저장 완료: {len(bjdong_codes)}개 시군구 → {bjdong_path}")


if __name__ == "__main__":
    main()
